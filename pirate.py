# Ask the user to input a password and store the response in the variable 'greeting'
greeting = input("Hello, possible pirate! What's the password? ")

# Check if the user's input (greeting) is exactly equal to "Arrr!"
if greeting == "Arrr!":
    # If the input is "Arrr!", print "Go away, pirate."
    print("Go away, pirate.")
else:
    # If the input is not "Arrr!", print "Greetings, hater of pirates!"
    print("Greetings, hater of pirates!")
